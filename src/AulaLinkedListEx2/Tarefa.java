package AulaLinkedListEx2;

public class Tarefa {
	
	private String nome;
	private TarefaTipo prioridade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public TarefaTipo getPrioridade() {
		return prioridade;
	}
	public void setPrioridade(TarefaTipo prioridade) {
		this.prioridade = prioridade;
	}
	
	

}
